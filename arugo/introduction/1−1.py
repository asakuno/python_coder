# 標準入力から値を受け取る
# num: int 型
num = int(input())

# 受け取った値を利用してコードを書いてください
if num == 1:
    print('one')
elif num == 2:
    print('two')
elif num == 3:
    print('three')
elif num == 4:
    print('four')
elif num == 5:
    print('five')
elif num == 6:
    print('six')
elif num == 7:
    print('seven')
elif num == 8:
    print('eight')
elif num == 9:
    print('nine')
else:
    print('ten')
